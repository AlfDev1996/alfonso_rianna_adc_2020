    private void btnCreateNowMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCreateNowMouseClicked
        // TODO add your handling code here:
        
        try {
            String name = auctionNameCreate.getText();
            String desc = auctionDescCreate.getText();
            if(name.equalsIgnoreCase("")|| desc.equalsIgnoreCase("")||name.chars().allMatch(Character::isDigit)|| desc.chars().allMatch(Character::isDigit))
                throw new CreateAuctionException("Il nome e la descrizione dell'asta non possono essere vuoti o valori unicamente numerici");
            double reservePrice = Double.parseDouble(reservePriceCreate.getText());
            Date expDate = new SimpleDateFormat("dd/MM/yyyy hh:mm").parse(auctionExpCreate.getText());
           
            
            if(expDate.before(new Date(System.currentTimeMillis() + 3600 * 1000)))
                throw new CreateAuctionException("La data di creazione deve essere successiva alla data corrente locale");
            
            if(reservePrice<=0)
                throw new CreateAuctionException("Il prezzo di riserva non puÃ² essere negativo o pari a 0");
            
            if(peerRif.createAuction(name, expDate, reservePrice, desc)){
                rifMain.outConsoleSet("Asta "+name+" Creata con successo");
                this.dispatchEvent(new WindowEvent(this, WindowEvent.WINDOW_CLOSING));
            }else{
                throw new CreateAuctionException("Errore nella Creazione Dell'asta");
            }
            
        }catch(CreateAuctionException er){
            JOptionPane.showMessageDialog(this, er.getMessage());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Errore Dati di Input");
            //Logger.getLogger(createAuctionFrame.class.getName()).log(Level.SEVERE, null, e);
        }
    }//GEN-LAST:event_btnCreateNowMouseClicked

